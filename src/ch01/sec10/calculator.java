public class calculator {
	public static void main(String[]args) {  // 메소드가있어야  변수나 다른 선언도 가능 
	int x;                                   // 변수 x 선언
	x = 1;                                   // 변수 x에 1을 저장
	int y= 2;                                // 변수 y를 선언하고 y에 2를 저장
	int result= x+y;                 // 변수 result 선언하고 x+y를 저장 
System.out.println(result);                  // 콘솔에 출력하는 메소드 호출 
    }
}